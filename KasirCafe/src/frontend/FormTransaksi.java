    private void SimpanButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SimpanButtonActionPerformed
        // TODO add your handling code here:
        Transaksi tr = new Transaksi();
        tr.setId_transaksi(Integer.parseInt(IdField.getText()));
        tr.setMenu((Menu) cmbMenu.getSelectedItem());        
        tr.getMenu().setHarga(Integer.parseInt(JumlahField.getText()));
        tr.setJumlah(Integer.parseInt(JumlahField.getText()));
        tr.setTotal_harga(Integer.parseInt(TotalHargaField.getText()));
        tr.setJumlah_bayar(Integer.parseInt(JumlahBayarField.getText()));
        tr.setKembalian(Integer.parseInt(KembalianField.getText()));
        tr.setTanggal(TanggalField.getText());
        tr.save();
        IdField.setText(Integer.toString(tr.getId_transaksi()));
        tampilkanData();
    }//GEN-LAST:event_SimpanButtonActionPerformed

