    private void TableTrMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TableTrMouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) TableTr.getModel();
        int row = TableTr.getSelectedRow();
        Transaksi tr = new Transaksi();
        
        tr = tr.getById(Integer.parseInt(model.getValueAt(row, 0).toString()));
        
        IdField.setText(String.valueOf(tr.getId_transaksi()));
        cmbMenu.getModel().setSelectedItem(tr.getMenu());
        HargaField.setText(String.valueOf(tr.getMenu().getHarga()));
        JumlahField.setText(String.valueOf(tr.getJumlah()));
        TotalHargaField.setText(String.valueOf(tr.getTotal_harga()));
        JumlahBayarField.setText(String.valueOf(tr.getJumlah_bayar()));
        JumlahBayarField.setEditable(false);
        KembalianField.setText(String.valueOf(tr.getKembalian()));
        TanggalField.setText(String.valueOf(tr.getTanggal()));
        
    }//GEN-LAST:event_TableTrMouseClicked

