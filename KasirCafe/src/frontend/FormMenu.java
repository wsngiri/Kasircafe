    public void tampilkanData() {
        String[] kolom = {"ID", "Nama","Kategori", "HArga"};
        ArrayList<Menu> list = new Menu().getAll();
        Object rowData[] = new Object[5];

        MenuTabel.setModel(new DefaultTableModel(new Object[][]{}, kolom));

        for (Menu men : list) {
            rowData[0] = men.getId_menu();
            rowData[1] = men.getNama();
            rowData[2] = men.getKat().getNama_kategori();
            rowData[3] = men.getHarga();
            ((DefaultTableModel) MenuTabel.getModel()).addRow(rowData);
        }
    }

    public void cari(String keyword) {
        String[] kolom = {"ID", "Nama", "Kategori", "Harga"};
        ArrayList<Menu> list = new Menu().search(keyword);
        Object rowData[] = new Object[5];

        MenuTabel.setModel(new DefaultTableModel(new Object[][]{}, kolom));

        for (Menu men : list) {
            rowData[0] = men.getId_menu();
            rowData[1] = men.getNama();
            rowData[2] = men.getKat().getNama_kategori();
            rowData[3] = men.getHarga();
            ((DefaultTableModel) MenuTabel.getModel()).addRow(rowData);
        }
    }

    public void tampilkanCmbKategori() {
        cmbKategori.setModel(new DefaultComboBoxModel(new Kategori().getAll().toArray()));
    }
