    private void MenuTabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_MenuTabelMouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) MenuTabel.getModel();
        int row = MenuTabel.getSelectedRow();
        Menu men = new Menu();
        
        men = men.getById(Integer.parseInt(model.getValueAt(row, 0).toString()));

        IdField.setText(String.valueOf(men.getId_menu()));
        NamaField.setText(men.getNama());
        cmbKategori.getModel().setSelectedItem(men.getKat());
        HargaField.setText(String.valueOf(men.getHarga()));
        
    }//GEN-LAST:event_MenuTabelMouseClicked

