    private void HapusButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_HapusButtonActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) MenuTabel.getModel();
        int row = MenuTabel.getSelectedRow();
        Menu men = new Menu().getById(Integer.parseInt(model.getValueAt(row, 0).toString()));
        men.delete();
        kosongkanForm();
        tampilkanData();
    }//GEN-LAST:event_HapusButtonActionPerformed

    private void SaveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveButtonActionPerformed
        // TODO add your handling code here:        
        Menu men = new Menu();
        men.setId_menu(Integer.parseInt(IdField.getText()));
        men.setNama(NamaField.getText());
        men.setKat((Kategori) cmbKategori.getSelectedItem());
        men.setHarga(Integer.parseInt(HargaField.getText()));
        men.save();
        IdField.setText(Integer.toString(men.getId_menu()));
        tampilkanData();
        
        
    }//GEN-LAST:event_SaveButtonActionPerformed

